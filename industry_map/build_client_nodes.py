def build():
    """构建所有目标客户节点数据"""
    count = 0
    for client_name, data in CLIENTS.items():
        chain_name = f"【客户】{client_name}"
        
        # 删除旧链
        ChainManager.delete_chain(chain_name)
        
        # 创建新链
        cid = ChainManager.create_chain(chain_name, data["desc"])
        
        # 添加环节
        for link_data in data["links"]:
            name, level, barrier, rate, desc, stocks = link_data
            lid = LinkManager.add_link(
                chain_name, name,
                level=level, barrier=barrier, rate=rate,
                description=desc,
                sort_order=link_data[0]  # 用索引
            )
            if lid:
                StockManager.add_stocks(chain_name, name, stocks)
        
        count += 1
        print(f"✅ {client_name} ({sum(len(l[5]) for l in data['links'])}只股票)")
    
    # 重新排序：客户链强制排到最前
    db = ChainDB.connect()
    cur = db.cursor()
    cur.execute("SELECT id, name FROM industry_chains WHERE name LIKE '【客户】%'")
    for i, r in enumerate(cur.fetchall()):
        cur.execute("UPDATE industry_chains SET sort_order = ? WHERE id = ?", (i, r['id']))
    db.commit()
    db.close()
    
    print(f"\n✅ 共构建 {count} 个目标客户产业链！")

if __name__ == '__main__':
    build()
