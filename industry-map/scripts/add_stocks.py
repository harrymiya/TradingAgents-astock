def stocks_to_json(stocks_map):
    """将{环节名: [(code, name), ...]}转为[node_name: [code, ...], ...]"""
    result = {}
    for link_name, stocks in stocks_map.items():
        result[link_name] = [s[0] for s in stocks]  # 只存6位代码
    return result

def build_name_map(stocks_map):
    """构建 code->name 映射"""
    result = {}
    for link_name, stocks in stocks_map.items():
        for code, name in stocks:
            result[code] = name
    return result

